alter table public.wards enable row level security;
alter table public.citizens enable row level security;
alter table public.auth_users enable row level security;
alter table public.officers enable row level security;
alter table public.complaints enable row level security;
alter table public.complaint_status_timeline enable row level security;
alter table public.jurisdiction_mappings enable row level security;

create policy "Public read states" on public.states for select using (true);
create policy "Public read districts" on public.districts for select using (true);
create policy "Public read areas" on public.areas for select using (true);
create policy "Public read wards" on public.wards for select using (true);

create policy "Citizen can read own profile" on public.citizens for select using (auth.uid()::text = citizen_id::text);
create policy "Citizen can insert own profile" on public.citizens for insert with check (true);

create policy "Auth users self read" on public.auth_users for select using (auth.uid()::text = auth_user_id::text);
create policy "Auth users self update" on public.auth_users for update using (auth.uid()::text = auth_user_id::text);

create policy "Officer can read own record" on public.officers for select using (auth.uid()::text = officer_id::text);
create policy "Officer can update own record" on public.officers for update using (auth.uid()::text = officer_id::text);

create policy "Citizen can read complaints" on public.complaints for select using (true);
create policy "Citizen can create complaints" on public.complaints for insert with check (true);
create policy "Officer can update complaints" on public.complaints for update using (true);

create policy "Read complaint timeline" on public.complaint_status_timeline for select using (true);
create policy "Insert complaint timeline" on public.complaint_status_timeline for insert with check (true);

create policy "Read jurisdiction mappings" on public.jurisdiction_mappings for select using (true);
create policy "Insert jurisdiction mappings" on public.jurisdiction_mappings for insert with check (true);
create policy "Update jurisdiction mappings" on public.jurisdiction_mappings for update using (true);

