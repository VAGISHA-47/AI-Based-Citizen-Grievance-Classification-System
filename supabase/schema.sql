-- JANSETU Supabase schema
-- PostgreSQL / Supabase-ready schema for JANSETU location hierarchy,
-- officer jurisdiction, complaints, and status timeline.

create extension if not exists "pgcrypto";

create table if not exists public.states (
  state_id bigint generated by default as identity primary key,
  state_name text not null unique,
  state_code text not null unique,
  created_at timestamptz not null default now()
);

create table if not exists public.districts (
  district_id bigint generated by default as identity primary key,
  district_name text not null,
  state_id bigint not null references public.states(state_id) on delete cascade,
  created_at timestamptz not null default now(),
  unique (state_id, district_name)
);

create table if not exists public.areas (
  area_id bigint generated by default as identity primary key,
  area_name text not null,
  district_id bigint not null references public.districts(district_id) on delete cascade,
  pincode text not null,
  created_at timestamptz not null default now(),
  unique (district_id, area_name)
);

create table if not exists public.wards (
  ward_id bigint generated by default as identity primary key,
  ward_number text not null,
  ward_name text not null,
  area_id bigint not null references public.areas(area_id) on delete cascade,
  created_at timestamptz not null default now(),
  unique (area_id, ward_number)
);

create table if not exists public.citizens (
  citizen_id uuid primary key default gen_random_uuid(),
  name text not null,
  phone text not null unique,
  email text unique,
  password_hash text not null,
  address text,
  created_at timestamptz not null default now()
);

create table if not exists public.officers (
  officer_id uuid primary key default gen_random_uuid(),
  badge_number text unique,
  name text not null,
  email text unique,
  password_hash text not null,
  phone text,
  role text not null default 'officer' check (role in ('officer', 'senior_officer', 'admin')),
  assigned_ward_id bigint references public.wards(ward_id) on delete set null,
  additional_ward_ids jsonb not null default '[]'::jsonb,
  district_id bigint references public.districts(district_id) on delete set null,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.auth_users (
  auth_user_id uuid primary key default gen_random_uuid(),
  phone text unique,
  email text unique,
  password_hash text not null,
  role text not null check (role in ('citizen', 'officer', 'senior_officer', 'admin')),
  citizen_id uuid references public.citizens(citizen_id) on delete set null,
  officer_id uuid references public.officers(officer_id) on delete set null,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.complaints (
  complaint_id uuid primary key default gen_random_uuid(),
  tracking_token text not null unique,
  citizen_id uuid references public.citizens(citizen_id) on delete set null,
  officer_id uuid references public.officers(officer_id) on delete set null,
  ward_id bigint references public.wards(ward_id) on delete set null,
  category text not null,
  text text not null,
  language text not null default 'en',
  lat numeric(10, 7),
  lng numeric(10, 7),
  address text,
  media_urls jsonb not null default '[]'::jsonb,
  ai_analysis jsonb not null default '{}'::jsonb,
  status text not null default 'pending' check (status in ('pending', 'assigned', 'in_progress', 'resolved', 'rejected', 'escalated')),
  sla_days integer not null default 7,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.complaint_status_timeline (
  timeline_id uuid primary key default gen_random_uuid(),
  complaint_id uuid not null references public.complaints(complaint_id) on delete cascade,
  status text not null,
  note text,
  changed_by uuid references public.officers(officer_id) on delete set null,
  created_at timestamptz not null default now()
);

create table if not exists public.jurisdiction_mappings (
  mapping_id uuid primary key default gen_random_uuid(),
  officer_id uuid not null references public.officers(officer_id) on delete cascade,
  ward_id bigint not null references public.wards(ward_id) on delete cascade,
  is_primary boolean not null default false,
  created_at timestamptz not null default now(),
  unique (officer_id, ward_id)
);

create index if not exists idx_districts_state_id on public.districts(state_id);
create index if not exists idx_areas_district_id on public.areas(district_id);
create index if not exists idx_wards_area_id on public.wards(area_id);
create index if not exists idx_officers_assigned_ward_id on public.officers(assigned_ward_id);
create index if not exists idx_officers_district_id on public.officers(district_id);
create index if not exists idx_auth_users_phone on public.auth_users(phone);
create index if not exists idx_auth_users_email on public.auth_users(email);
create index if not exists idx_complaints_tracking_token on public.complaints(tracking_token);
create index if not exists idx_complaints_officer_id on public.complaints(officer_id);
create index if not exists idx_complaints_ward_id on public.complaints(ward_id);
create index if not exists idx_status_timeline_complaint_id on public.complaint_status_timeline(complaint_id);
create index if not exists idx_jurisdiction_officer_id on public.jurisdiction_mappings(officer_id);
create index if not exists idx_jurisdiction_ward_id on public.jurisdiction_mappings(ward_id);

